    private void bt_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registroActionPerformed
        Registro registro = new Registro();
        Sesion sesion = new Sesion();
        
        if (menu_inicio == true){
            this.bt_registro.setText("Iniciar sesion");
            
            CambiarPanel(registro);
            menu_inicio = false;
            menu_registro = true;
        }
        else{
            if (menu_registro == true){
                this.bt_registro.setText("Registrarse");
                
                CambiarPanel(sesion);
                menu_inicio = true;
                menu_registro = false;
            }
        }
        
    }//GEN-LAST:event_bt_registroActionPerformed

